//package SeamCarving;

public class Pixel{
	public int x;
	public int y;
	
	public Pixel(int a, int b) {
		x = a;
		y = b;
	}
	
	public void setX(int a){
		x = a;
	}
	
	public void setY(int b){
		y = b;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
}